package com.ets.bfd.entity;

public class CheckInCheckOut {

    private String user_id;
    private String one_day_tour_id;
    private String parameter_id;
    private String office_id;
    private String spot_id;
    private String code;
    private String message;

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getOne_day_tour_id() {
        return one_day_tour_id;
    }

    public void setOne_day_tour_id(String one_day_tour_id) {
        this.one_day_tour_id = one_day_tour_id;
    }

    public String getParameter_id() {
        return parameter_id;
    }

    public void setParameter_id(String parameter_id) {
        this.parameter_id = parameter_id;
    }

    public String getOffice_id() {
        return office_id;
    }

    public void setOffice_id(String office_id) {
        this.office_id = office_id;
    }

    public String getSpot_id() {
        return spot_id;
    }

    public void setSpot_id(String spot_id) {
        this.spot_id = spot_id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
